package org.lofar.data.catalog;

public class Classification {
	private int classId;
	private short type;

	public int getClassId() {
		return classId;
	}

	public short getType() {
		return type;
	}

	public void setClassId(int classId) {
		this.classId = classId;
	}

	public void setType(short type) {
		this.type = type;
	}

}
