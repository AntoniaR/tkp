package org.lofar.data;

public class Beam {
	private int beamId;
	private int obsId;
	private int resId;

	public int getBeamId() {
		return beamId;
	}

	public int getObsId() {
		return obsId;
	}

	public int getResId() {
		return resId;
	}

	public void setBeamId(int beamId) {
		this.beamId = beamId;
	}

	public void setObsId(int obsId) {
		this.obsId = obsId;
	}

	public void setResId(int resId) {
		this.resId = resId;
	}

}
