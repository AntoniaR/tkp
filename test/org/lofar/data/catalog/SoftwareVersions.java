package org.lofar.data.catalog;

public class SoftwareVersions {
	private int swVrsId;
	private int reposId;
	private int dbId;

	public int getSwVrsId() {
		return swVrsId;
	}

	public int getReposId() {
		return reposId;
	}

	public int getDbId() {
		return dbId;
	}

	public void setSwVrsId(int swVrsId) {
		this.swVrsId = swVrsId;
	}

	public void setReposId(int reposId) {
		this.reposId = reposId;
	}

	public void setDbId(int dbId) {
		this.dbId = dbId;
	}

}
